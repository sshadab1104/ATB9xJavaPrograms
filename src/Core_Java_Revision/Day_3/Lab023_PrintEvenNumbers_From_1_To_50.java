package Core_Java_Revision.Day_3;

public class Lab023_PrintEvenNumbers_From_1_To_50 {
    public static void main(String[] args) {
        // Program 3: Print even numbers from 1 to 50
        // Use for loop
        for (int i=0; i<=50; i++){
            if (i % 2 == 0) {
                System.out.println(i);
            }

        }
    }
}
