package Core_Java_Revision.Day_1;

public class Lab003_TypeCasting {
    public static void main(String[] args) {
        // Practice 3: Type Casting
        // Convert:
        // int → double
        // double → int

        int a = 10;
        double b = a;
        System.out.println(b);

        double c = 20.5;
        int d = (int)c;
        System.out.println(d);
    }
}
